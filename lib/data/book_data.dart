// Helper function to get books by category
List<Book> getBooksByCategory(String category) {
  if (category == 'all') return allBooks;
  return allBooks.where((book) => book.category == category).toList();
}

// Helper function to get free books
List<Book> getFreeBooks() {
  return allBooks.where((book) => book.isFree).toList();
}

// Helper function to get paid books
List<Book> getPaidBooks() {
  return allBooks.where((book) => !book.isFree).toList();
}